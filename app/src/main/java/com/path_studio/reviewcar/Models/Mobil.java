package com.path_studio.reviewcar.Models;

public class Mobil {

    private String merk_mobil;
    private String tipe_mobil;
    private String range_harga;
    private String kapasitas_mesin;
    private String tempat_duduk;
    private String jenis_transmisi;

    private String photo;

    public String getPhoto() {
        return photo;
    }

    public void setPhoto(String photo) {
        this.photo = photo;
    }

    public String getMerk_mobil() {
        return merk_mobil;
    }

    public void setMerk_mobil(String merk_mobil) {
        this.merk_mobil = merk_mobil;
    }

    public String getTipe_mobil() {
        return tipe_mobil;
    }

    public void setTipe_mobil(String tipe_mobil) {
        this.tipe_mobil = tipe_mobil;
    }

    public String getRange_harga() {
        return range_harga;
    }

    public void setRange_harga(String range_harga) {
        this.range_harga = range_harga;
    }

    public String getKapasitas_mesin() {
        return kapasitas_mesin;
    }

    public void setKapasitas_mesin(String kapasitas_mesin) {
        this.kapasitas_mesin = kapasitas_mesin;
    }

    public String getTempat_duduk() {
        return tempat_duduk;
    }

    public void setTempat_duduk(String tempat_duduk) {
        this.tempat_duduk = tempat_duduk;
    }

    public String getJenis_transmisi() {
        return jenis_transmisi;
    }

    public void setJenis_transmisi(String jenis_transmisi) {
        this.jenis_transmisi = jenis_transmisi;
    }
}
